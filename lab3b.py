def sum_numbers(a, b):
    return a + b

def subtract_numbers(a, b):
    return a - b

def multiply_numbers(a, b):
    return a * b

if __name__ == '__main__':
    print(sum_numbers(10, 5))      # 15
    print(subtract_numbers(10, 5)) # 5
    print(multiply_numbers(10, 5)) # 50
